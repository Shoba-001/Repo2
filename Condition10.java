package class4;

public class Condition10 {
public static void main(String args[]) {
	char letter = 'b';
	
	if(letter=='a') {
		System.out.println("Apple");
	}
	else if(letter=='b') {
		System.out.println("Ball");
	}
	else if(letter=='c') {
		System.out.println("cat");
	}
	else if(letter=='d') {
		System.out.println("Doll");
	}
	else {
		System.out.println("other alphabets");
	}
	
}
}
