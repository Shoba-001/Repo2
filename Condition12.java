package class4;

public class Condition12 {
	public static void main(String args[]) {
		String day="Monday";
		switch(day) {
		case "Sunday" :System.out.println("Firstday");
		break;
		case "Monday" :System.out.println("Secondday");
		break;
		case "Tuesday" :System.out.println("Thirdday");
		break;
		default : System.out.println("Not a day");
		break;
		}
		
	}
}
