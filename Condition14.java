package class4;

public class Condition14 {
	public static void main(String args[]) {
		String month="April";
		switch(month) {
		case "january":System.out.println("FirstMonth of the year");
		break;
		case "Feb":System.out.println("SecondMonth of the year");
		break;
		case "March": System.out.println("ThirdMonth of the year");
		break;
		case "April":System.out.println("FourthMonth of the year");
		break;
		case "May":System.out.println("FifthMonth of the year");
		break;
		case "June":System.out.println("SixthMonth of the year");
		break;
		case "July":System.out.println("SeventhMonth of the year");
		break;
		case "August":System.out.println("EighthMonth of the year");
		break;
		case "September":System.out.println("NinethMonth of the year");
		break;
		case "October":System.out.println("TenthMonth of the year");
		break;
		case "November":System.out.println("EleventhMonth of the year");
		break;
		case "December":System.out.println("TwelthMonth of the year");
		break;
		default:System.out.println("Not an year");
		break;
		}
	}

}
